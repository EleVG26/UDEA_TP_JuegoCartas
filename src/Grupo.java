/**
 * La enumeración Grupo representa los diferentes tamaños de agrupaciones
 * de cartas que pueden formarse en el juego.
 * Incluye desde agrupaciones vacías hasta grupos de diez cartas.
 */
public enum Grupo {
    VACIO,
    NON,
    PAR,
    TERNA,
    CUARTA,
    QUINTA,
    SEXTA,
    SEPTIMA,
    OCTAVA,
    NOVENA,
    DECIMA
}
