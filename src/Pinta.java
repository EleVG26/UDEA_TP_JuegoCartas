/**
 * La enumeración Pinta representa los cuatro palos o pintas
 * de una baraja inglesa. Cada valor corresponde a una de las
 * cuatro pintas: trébol, pica, corazón y diamante.
 */
public enum Pinta {
    TREBOL, // Trébol, de color negro
    PICA, // Pica o espada, de color negro
    CORAZON, // Corazón, de color rojo
    DIAMANTE // Diamante, de color rojo
}
